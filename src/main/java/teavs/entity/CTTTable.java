package teavs.entity;

public class CTTTable {
	String course_num;
	String course_name;
	String clazz_num;
	String clazz_name;
	String teacher_num;
	String user_name;

	public String getCourse_num() {
		return course_num;
	}

	public void setCourse_num(String course_num) {
		this.course_num = course_num;
	}

	public String getCourse_name() {
		return course_name;
	}

	public void setCourse_name(String course_name) {
		this.course_name = course_name;
	}

	public String getClazz_num() {
		return clazz_num;
	}

	public void setClazz_num(String clazz_num) {
		this.clazz_num = clazz_num;
	}

	public String getClazz_name() {
		return clazz_name;
	}

	public void setClazz_name(String clazz_name) {
		this.clazz_name = clazz_name;
	}

	public String getTeacher_num() {
		return teacher_num;
	}

	public void setTeacher_num(String teacher_num) {
		this.teacher_num = teacher_num;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

}
